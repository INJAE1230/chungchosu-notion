import { generateObject } from "ai";
import { z } from "zod";
import { model } from "./ai";

const workLogSchema = z.object({
  entries: z.array(
    z.object({
      title: z.string().describe("업무 제목 (간결하게)"),
      date: z.string().describe("날짜 (YYYY-MM-DD 형식)"),
      project: z.enum(["업무", "개인일정"]).describe("프로젝트 분류"),
      status: z.enum(["예정", "진행 중", "완료"]).describe("진행 상태"),
      content: z.string().describe("업무 상세 내용"),
      tags: z
        .array(z.enum(["회의", "개발", "기획", "리뷰", "버그"]))
        .describe("관련 태그"),
      hours: z.number().nullable().describe("소요 시간 (시간 단위)"),
      link: z.string().nullable().describe("관련 링크 URL"),
    })
  ),
});

export async function parseMemoText(text: string, today: string) {
  const { object } = await generateObject({
    model,
    schema: workLogSchema,
    prompt: `당신은 업무일지 파싱 도우미입니다. 사용자가 자유롭게 작성한 메모를 구조화된 업무일지 항목으로 변환해주세요.
한국어 구어체, 줄임말, 오타, 비격식 표현도 모두 이해해야 합니다.

오늘 날짜: ${today} (요일도 계산에 활용하세요)

━━━ 업무 분리 ━━━
- 하나의 메모에 여러 업무가 있으면 반드시 각각 별도 항목으로 분리
- 접속사/나열로 연결된 업무: "~하고", "~한 다음", "~끝나고", "그리고", "또", "이랑", "랑", 쉼표(,), 줄바꿈
- 시간대별 구분: "오전에 A 오후에 B" → 2건, "아침에 A 점심에 B 저녁에 C" → 3건
- 동일 업무가 여러 날이면 날짜별로 각각 1건씩 (예: "월화수 회의" → 3건)

━━━ 날짜 인식 ━━━
상대 날짜:
- 오늘/금일 → 오늘
- 내일/낼/명일 → +1일
- 모레/내일모레 → +2일
- 글피/글피날 → +3일
- 어제/전일 → -1일
- 그저께/그끄저께/엊그제 → -2일
- 지지난주/지난달/이번달/다음달 등 → 기준으로 계산

요일 기반:
- "월요일", "화요일", ... "일요일" / "월", "화", ... "일"
- "이번 주 수요일", "이번주 수", "금주 목요일"
- "다음 주 월요일", "다음주 월", "차주 화요일"
- "지난주 금요일", "저번주 수", "전주 목"
- "이번 금요일", "돌아오는 월요일", "오는 수요일"

날짜 범위 (각 날짜별 별도 항목):
- "월~금", "월요일부터 금요일까지", "월화수목금"
- "18일~20일", "18일부터 20일까지", "18-20일"
- "3일간", "3일 동안", "사흘간", "이틀간", "일주일간"
- "이번 주 내내", "이번 주 전부", "한 주 내내"
- "오늘부터 금요일까지", "내일부터 3일간"

한국식 날짜:
- "6월 20일", "6/20", "6.20", "0620"
- "2026년 6월 20일", "2026-06-20", "2026.06.20"
- "20일", "20일날" → 이번 달 기준
- "다음 달 5일", "지난달 15일"

날짜 언급 없으면 → 오늘 날짜

━━━ 시간 인식 ━━━
- "2시간", "2시간 반" → 2.5, "1시간 반" → 1.5
- "30분" → 0.5, "45분" → 0.75, "90분" → 1.5
- "반나절" → 4, "하루 종일/종일/온종일" → 8, "반일" → 4
- "오전 내내" → 4, "오후 내내" → 4
- "10시~12시", "10시부터 12시" → 2시간
- "2시간짜리", "약 3시간", "한 2시간" → 숫자 추출
- 시간 언급 없으면 null

━━━ 프로젝트 분류 ━━━
"개인일정":
- 연차/반차/오전반차/오후반차/휴가/여름휴가/연월차/대체휴무/경조사
- 병가/병원/진료/검진/건강검진/치과/안과/한의원
- 약속/모임/동창회/회식(업무 외)/생일/기념일/결혼식/장례식
- 이사/은행/관공서/우체국/주민센터/면허/여권
- 운동/헬스/필라테스/요가/수영/등산/조깅
- 취미/여행/쇼핑/미용실/네일
- 등하교/학원/시험/자격증/공부/수강
- 가족/육아/어린이집/유치원/학교행사
"업무": 위에 해당하지 않는 모든 것 (회사, 프로젝트, 클라이언트 등)

━━━ 진행상태 ━━━
"완료":
- ~했다/했음/함/끝/끝냄/끝남/마침/마무리/완료/다했다/처리함/해결/해결함
- ~했어/했어요/했습니다/마쳤어/끝났어/다끝남/완
- 과거형 서술: "A사 미팅 다녀옴", "보고서 제출함", "수정 완료"
"예정":
- ~할 예정/예정/계획/해야/해야함/해야됨/할 거/할거야/할것
- ~하기로/잡혀있음/잡힘/스케줄/일정/예약
- 미래 날짜 + 동사: "내일 미팅", "금요일 발표", "다음주 출장"
- ~할 듯/할 수도/할지도/생각 중
"진행 중":
- ~하는 중/진행 중/진행중/작업 중/작업중/처리 중/처리중
- ~하고 있다/하고있음/하는중임/달리는중/돌리는중
- 현재 진행: "지금 개발 중", "테스트 돌리는 중", "미팅 중"

상태 판단 우선순위: 명시적 키워드 > 시제(과거=완료, 미래=예정) > 기본값(진행 중)

━━━ 태그 ━━━
"회의": 미팅/회의/통화/콜/전화/화상/줌/zoom/teams/면담/상담/인터뷰/발표/PT/프레젠테이션/세미나/워크숍/스크럼/스탠드업/데일리/위클리/싱크/sync/킥오프/브리핑
"개발": 코딩/개발/구현/배포/deploy/릴리즈/release/머지/merge/커밋/commit/PR/풀리퀘/테스트/QA/디버깅/디버그/API/서버/프론트/백엔드/DB/마이그레이션/인프라/CI/CD/모니터링
"기획": 기획/설계/문서/문서화/정리/보고서/제안서/스펙/요구사항/와이어프레임/목업/디자인/UX/UI/리서치/분석/조사/벤치마킹/전략/로드맵/마일스톤
"리뷰": 리뷰/검토/피드백/회고/레트로/코드리뷰/CR/1on1/원온원/면담/평가/중간점검/데모
"버그": 버그/에러/수정/핫픽스/hotfix/장애/이슈/오류/트러블슈팅/긴급/패치/롤백/rollback/복구/incident

태그가 여러 개 해당되면 모두 포함 (예: "버그 수정 후 코드리뷰" → ["개발", "버그", "리뷰"])

━━━ 기타 ━━━
- 링크(URL)가 있으면 link에 넣고, 없으면 null
- content에는 원본 메모에서 해당 업무의 상세 맥락을 넣으세요
- 오타/줄임말 유추: "미팅갔다옴" → 미팅, "코리" → 코드리뷰, "배포함" → 배포
- 이모지도 참고: 🏥→병원, ✈️→출장/여행, 🏖️→휴가, 💻→개발, 📝→문서
- 카카오톡/메신저 스타일 입력도 처리: "ㅇㅇ 내일 연차", "ㄱㄱ 회의 10시"

사용자 메모:
${text}`,
  });

  return object.entries;
}
